
// 函数：获取法宝文件(Cấp 1)
string get_talisman_file()
{
        int p = random(10000);
        if( p < 650 ) return "/item/std/9511";
        else if( p < 750 ) return "/item/std/9521";
        else if( p < 1750 ) return "/item/std/9531";
        else if( p < 3050 ) return "/item/std/9541";
        else if( p < 3550 ) return "/item/std/9551";
        else if( p < 4850 ) return "/item/std/9561";
        else if( p < 6100 ) return "/item/std/9571";
        else if( p < 6500 ) return "/item/std/9581";
        else if( p < 10000 ) return "/item/std/9601";
}

